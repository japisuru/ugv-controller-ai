package math;

public class Velocity {
    private double vX;
    private double vY;
    private double vZ;

    /**
     * @return the vX
     */
    public double getvX() {
        return vX;
    }

    /**
     * @param vX the vX to set
     */
    public void setvX(double vX) {
        this.vX = vX;
    }

    /**
     * @return the vY
     */
    public double getvY() {
        return vY;
    }

    /**
     * @param vY the vY to set
     */
    public void setvY(double vY) {
        this.vY = vY;
    }

    /**
     * @return the vZ
     */
    public double getvZ() {
        return vZ;
    }

    /**
     * @param vZ the vZ to set
     */
    public void setvZ(double vZ) {
        this.vZ = vZ;
    }
}


